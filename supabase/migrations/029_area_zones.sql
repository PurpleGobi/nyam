-- 생활권 정의 테이블: 중심 좌표 + 반경으로 영역 판정
-- 필터 시 haversine(restaurant.lat, restaurant.lng, zone.lat, zone.lng) <= zone.radius_m

CREATE TABLE area_zones (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name VARCHAR(50) NOT NULL,          -- 생활권 이름 (강남, 홍대, 을지로 등)
  city VARCHAR(50) NOT NULL,          -- 소속 도시 (서울, 부산 등)
  lat DOUBLE PRECISION NOT NULL,      -- 중심 위도
  lng DOUBLE PRECISION NOT NULL,      -- 중심 경도
  radius_m INT NOT NULL DEFAULT 1500, -- 반경 (미터)
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_area_zones_city ON area_zones(city);

