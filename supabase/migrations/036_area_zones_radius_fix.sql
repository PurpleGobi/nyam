-- 생활권 반경 재조정 + 대형 생활권 다중 원 전환
-- 원칙: 해당 생활권의 식당/카페 밀집 지역을 충분히 포함 + 약간의 여유
-- 대형 생활권(구 단위)은 다중 원으로 분할하여 인접 구 침범 방지

-- ================================================================
-- 1. 대형 생활권: 단일 원 → 다중 원 전환
-- ================================================================

-- ── 강남: 1원(1800m) → 3원 ──
-- 역삼/삼성/청담/압구정/신사가 강남구 북쪽을 이미 커버하므로,
-- 강남 다중 원은 강남역 상권 + 남쪽 미커버 지역(대치·도곡·개포·수서)을 담당
DELETE FROM area_zones WHERE name = '강남' AND city = '서울';
INSERT INTO area_zones (name, city, lat, lng, radius_m) VALUES
  ('강남', '서울', 37.4980, 127.0280, 1500),  -- ① 강남역·논현 (강남대로 중심, 학동사거리)
  ('강남', '서울', 37.4940, 127.0600, 1500),  -- ② 대치·도곡 (학원가, 양재천 북쪽)
  ('강남', '서울', 37.4780, 127.0720, 1500);  -- ③ 개포·수서 (개포동, 수서역, 세곡동)

-- ── 여의도: 1원(1600m) → 2원 ──
-- 섬이 E-W로 길어서 단일 원 비효율
DELETE FROM area_zones WHERE name = '여의도' AND city = '서울';
INSERT INTO area_zones (name, city, lat, lng, radius_m) VALUES
  ('여의도', '서울', 37.5225, 126.9260, 1300),  -- ① 동쪽: IFC·여의도공원·63빌딩
  ('여의도', '서울', 37.5285, 126.9090, 1200);  -- ② 서쪽: 국회의사당·MBC·방송국

-- ================================================================
-- 2. 나머지 생활권: 반경 보정 (단일 원 유지)
-- ================================================================

UPDATE area_zones SET radius_m = CASE name
  -- ── 강남권 ──
  WHEN '역삼'     THEN 1300  -- 1200→1300 (역삼역~테헤란로 상권)
  WHEN '삼성'     THEN 1400  -- 1300→1400 (코엑스~봉은사 포함)
  WHEN '청담'     THEN 1300  -- 1200→1300 (청담사거리~도산대로)
  WHEN '압구정'   THEN 1300  -- 1200→1300 (로데오거리~압구정역)
  WHEN '신사'     THEN 1200  -- 1000→1200 (신사역~가로수길 입구)
  WHEN '가로수길' THEN 1000  -- 600→1000 (가로수길+주변 골목 식당)
  WHEN '서초'     THEN 1500  -- 유지

  -- ── 잠실·송파 ──
  WHEN '잠실'     THEN 1700  -- 유지 (롯데월드~석촌호수)
  WHEN '송파'     THEN 1500  -- 유지

  -- ── 마포권 ──
  WHEN '홍대'     THEN 1500  -- 1300→1500 (홍대 상권 매우 넓음)
  WHEN '연남'     THEN 1200  -- 1000→1200 (경의선숲길+연남동)
  WHEN '합정'     THEN 1200  -- 1000→1200 (합정역~망원동 경계)
  WHEN '망원'     THEN 1200  -- 1000→1200 (망원시장+망리단길)
  WHEN '상수'     THEN 1100  -- 900→1100 (상수역~당인리 상권)

  -- ── 용산권 ──
  WHEN '이태원'   THEN 1500  -- 1300→1500 (이태원대로+경리단길+우사단길)
  WHEN '한남'     THEN 1300  -- 1100→1300 (한남동 전체+블루스퀘어)
  WHEN '녹사평'   THEN 1000  -- 800→1000 (녹사평역~해방촌)

  -- ── 종로·중구 ──
  WHEN '을지로'   THEN 1200  -- 유지 (힙지로 상권)
  WHEN '명동'     THEN 1100  -- 900→1100 (명동 쇼핑가+주변 식당)
  WHEN '종로'     THEN 1400  -- 유지 (종로 전체)
  WHEN '광화문'   THEN 1200  -- 1000→1200 (광화문~서촌 경계)
  WHEN '북촌'     THEN 1000  -- 900→1000 (북촌한옥마을+계동)
  WHEN '서촌'     THEN 1000  -- 800→1000 (통인시장~경복궁 서쪽)
  WHEN '대학로'   THEN 1100  -- 1000→1100 (혜화역~이화동)

  -- ── 성동·광진 ──
  WHEN '성수'     THEN 1400  -- 유지 (성수 카페거리+서울숲)
  WHEN '건대'     THEN 1200  -- 유지 (건대입구 상권)
  WHEN '왕십리'   THEN 1200  -- 1000→1200 (왕십리역~비트플렉스)
  WHEN '한양대'   THEN 1000  -- 800→1000 (한양대 후문 상권)

  -- ── 영등포 ──
  WHEN '영등포'   THEN 1200  -- 유지

  -- ── 기타 ──
  WHEN '신촌'     THEN 1300  -- 1100→1300 (신촌역~이대 방면)

  ELSE radius_m
END
WHERE city = '서울';
